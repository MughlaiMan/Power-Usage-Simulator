package groupProjectCode;

public class Location {

	public String locationId;
	public int numApp;
	
	
	public Location(String locID, int numApp) {
		this.locationId = locID;
		this.numApp = numApp;
	}
	
	
	
	
	
	
	
	
	
	public String getlocID() {
		return this.locationId;
	}
	
	public int getNumApp() {
		return this.numApp;
	}
	
	
	
}